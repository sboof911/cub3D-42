#include "../cub3d.h"

int		ft_atoi(const char *s)
{
	int		x;
	int		t;

	x = 0;
	t = 1;
	if (!*s)
		return (0);
	while (*s == ' ' || *s <= '\n' || *s == '\v' || *s == '\r'
			|| *s == '\t' || *s == '\f')
		s++;
	if (*s == '-')
		t *= -1;
	if (*s == '+' || *s == '-')
		s++;
	while (*s >= '0' && *s <= '9' && *s)
	{
		if (t > 0 && x < 0)
			return (-1);
		if (t < 0 && x < 0)
			return (0);
		x = (x * 10) + *s - '0';
		s++;
	}
	return (x * t);
}
