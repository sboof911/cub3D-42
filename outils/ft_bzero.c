#include "../cub3d.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*b;
	int				i;

	i = 0;
	b = s;
	while (i < (int)n)
	{
		b[i] = (unsigned char)'\0';
		i++;
	}
}
